package ReddygaruExamples;

public class Expense {
    private int expenceId;
    private String expenceName;
    private int expencePrice;
    private String expenceMonth;

    public int getExpenceId() {
        return expenceId;
    }

    public void setExpenceId(int expenceId) {
        this.expenceId = expenceId;
    }

    public String getExpenceName() {
        return expenceName;
    }

    public void setExpenceName(String expenceName) {
        this.expenceName = expenceName;
    }

    public int getExpencePrice() {
        return expencePrice;
    }

    public void setExpencePrice(int expencePrice) {
        this.expencePrice = expencePrice;
    }

    public String getExpenceMonth() {
        return expenceMonth;
    }

    public void setExpenceMonth(String expenceMonth) {
        this.expenceMonth = expenceMonth;
    }
}
