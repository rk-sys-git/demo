package Indane;

public class Identy {
    private String doorno;
    private String steet;
    private String city;
    private int pincdode;

    public String getDoorno() {
        return doorno;
    }

    public void setDoorno(String doorno) {
        this.doorno = doorno;
    }

    public String getSteet() {
        return steet;
    }

    public void setSteet(String steet) {
        this.steet = steet;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getPincdode() {
        return pincdode;
    }

    public void setPincdode(int pincdode) {
        this.pincdode = pincdode;
    }
}
