package MapExamples;

public class Pincode {
    private int houseno;
    private String street;
    private String city;
    private String godavari;

    public int getHouseno() {
        return houseno;
    }

    public void setHouseno(int houseno) {
        this.houseno = houseno;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getGodavari() {
        return godavari;
    }

    public void setGodavari(String godavari) {
        this.godavari = godavari;
    }
}
